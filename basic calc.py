num1 = eval(input("Enter number: "))
num2 = eval(input("Enter number: "))
op = input("Enter operator: ")

if op == "+":
    print("The answer is: ", num1 + num2)

elif op == "-":
    print("The answer is: ", num1 - num2)

elif op == "*":
    print("The answer is: ", num1 * num2)

elif op == "/":
    print("The answer is: ", num1 / num2)

elif op == "**":
    print("The answer is: ", num1 ** num2)

else:
    print("comrade no be so o")